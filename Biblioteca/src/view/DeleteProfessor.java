    public static void readJTProfessor() {   
        Tabela_professor.removeAll();
        DefaultTableModel model = (DefaultTableModel) Tabela_professor.getModel();
        model.setNumRows(0);
        ProfessorDAOimplBD inst = new ProfessorDAOimplBD();

        for (Professor p : inst.getAllProfessor()) {
            model.addRow(new Object[]{
                p.getNome(),
                p.getMatricula(),
                p.getRg(),
                p.getDisciplina(),
                p.getEndereco(),
                p.getSenha(),
                p.getEmail(),
                p.getQuant_livros_locados()
            });
        }   
        Tabela_professor.setModel(model);
    }


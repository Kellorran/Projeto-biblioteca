    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        String matricula = txtMatricula.getText();
        DefaultTableModel dtmProfessor = (DefaultTableModel) Tabela_professor.getModel();
        Tabela_professor.removeAll();
        dtmProfessor.setNumRows(0);
        ProfessorDAOimplBD instance = new ProfessorDAOimplBD();
        Professor pf = instance.get(matricula);
        dtmProfessor.addRow(new Object[]{
            pf.getNome(),
            pf.getMatricula(),
            pf.getRg(),
            pf.getEndereco(),
            pf.getDisciplina(),
            pf.getSenha(),
            pf.getEmail(),
            pf.getQuant_livros_locados()
        });
    }//GEN-LAST:event_SearchActionPerformed


    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        String matricula = txtMatricula.getText();
        DefaultTableModel dtmAlunos = (DefaultTableModel) Tabela_alunos.getModel();
        Tabela_alunos.removeAll();
        dtmAlunos.setNumRows(0);
        AlunoDAOimplBD instance = new AlunoDAOimplBD();
        Aluno aln = instance.get(matricula);
        dtmAlunos.addRow(new Object[]{
                aln.getNome(),
                aln.getMatricula(),
                aln.getRg(),
                aln.getCurso(),
                aln.getSerie(),
                aln.getEndereco(),
                aln.getMonitoria(),
                aln.getSenha(),
                aln.getEmail(),
                aln.getQuant_livros_locados()
            });
    }//GEN-LAST:event_SearchActionPerformed


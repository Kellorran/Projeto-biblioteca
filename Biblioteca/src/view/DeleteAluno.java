    public static void readJTAlunosDel() {
        Tabela_alunos.removeAll();
        DefaultTableModel model = (DefaultTableModel) Tabela_alunos.getModel();
        model.setNumRows(0);
        AlunoDAOimplBD inst = new AlunoDAOimplBD();

        for (model.bean.Aluno a : inst.getAllAluno()) {
            model.addRow(new Object[]{
                a.getNome(),
                a.getMatricula(),
                a.getRg(),
                a.getCurso(),
                a.getSerie(),
                a.getEndereco(),
                a.getMonitoria(),
                a.getSenha(),
                a.getEmail(),
                a.getQuant_livros_locados()
            });
        }
        
        Tabela_alunos.setModel(model);
    }
    

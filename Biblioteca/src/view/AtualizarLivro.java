    private void AtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarActionPerformed

        String titulo = txtTitulo.getText();
        String autor = txtAutor.getText();
        String materia = txtMateria.getText();
        String genero = txtGenero.getText();
        String estado = txtEstado.getText();
        int codigo = Integer.parseInt(txtCodigo.getText());
        String isbn = txtIsbn.getText();
        int status = Integer.parseInt(txtStatus.getText());
        int quantidade = Integer.parseInt(txtQuantidade.getText());
        String nomeProfessor = null;
        String disciplinaProfessor = null;
        
        Livro l = new Livros_sugeridos(titulo,autor,materia,genero,estado,codigo,isbn,status,quantidade,nomeProfessor,disciplinaProfessor);
        LivroDAOimplBD instance = new LivroDAOimplBD();
        instance.update(l);
        if(Tabela_livro.getSelectedRow()!=-1){
            Tabela_livro.setValueAt(l.getTitulo(),Tabela_livro.getSelectedRow(),0);
            Tabela_livro.setValueAt(l.getAutor(),Tabela_livro.getSelectedRow(),1);
            Tabela_livro.setValueAt(l.getMateria_relacionada(),Tabela_livro.getSelectedRow(),2);
            Tabela_livro.setValueAt(l.getGenero(),Tabela_livro.getSelectedRow(),3);
            Tabela_livro.setValueAt(l.getEstado_do_livro(),Tabela_livro.getSelectedRow(),4);
            Tabela_livro.setValueAt(l.getCodigo(),Tabela_livro.getSelectedRow(),5);
            Tabela_livro.setValueAt(l.getIsbn(),Tabela_livro.getSelectedRow(),6);
            Tabela_livro.setValueAt(l.getStatus(),Tabela_livro.getSelectedRow(),7);
            Tabela_livro.setValueAt(l.getQuant_livros(),Tabela_livro.getSelectedRow(),8);
        }
        this.dispose();
    }//GEN-LAST:event_AtualizarActionPerformed

    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        int codigo = Integer.parseInt(txtSearch.getText());
        DefaultTableModel dtmLivro = (DefaultTableModel) Tabela_livro.getModel();
        Tabela_livro.removeAll();
        dtmLivro.setNumRows(0);
        LivroDAOimplBD instance = new LivroDAOimplBD();
        Livro l = instance.get(codigo);
        dtmLivro.addRow(new Object[]{
                l.getTitulo(),
                l.getAutor(),
                l.getCodigo(),
                l.getIsbn(),
                l.getGenero(),
                l.getMateria_relacionada(),
                l.getEstado_do_livro(),
                l.getStatus(),
                l.getQuant_livros()
            });
    }//GEN-LAST:event_SearchActionPerformed


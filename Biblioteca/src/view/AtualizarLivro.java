    public void readJTLivros() {        
        Tabela_livro.removeAll();
        DefaultTableModel model = (DefaultTableModel) Tabela_livro.getModel();
        model.setNumRows(0);
        LivroDAOimplBD inst = new LivroDAOimplBD();

        for (Livro l : inst.getAllLivro()) {
            model.addRow(new Object[]{
                l.getTitulo(),
                l.getAutor(),
                l.getCodigo(),
                l.getIsbn(),
                l.getGenero(),
                l.getMateria_relacionada(),
                l.getEstado_do_livro(),
                l.getStatus(),
                l.getQuant_livros()
            });
        }
        
        Tabela_livro.setModel(model);
    }


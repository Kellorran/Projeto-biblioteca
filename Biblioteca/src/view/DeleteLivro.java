    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        
        if (Tabela_livro.getSelectedRow() != -1){
            DefaultTableModel dtmLivro = (DefaultTableModel) Tabela_livro.getModel();
            dtmLivro.removeRow(Tabela_livro.getSelectedRow());
        }else {
            JOptionPane.showMessageDialog(null, "Selecione a coluna !!!");
        }
        int codigo = Integer.parseInt(txtSearch.getText());
        LivroDAOimplBD instance = new LivroDAOimplBD();
        instance.delete(codigo);
    }//GEN-LAST:event_ExcluirActionPerformed


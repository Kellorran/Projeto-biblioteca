    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        int codigo = Integer.parseInt(txtSearch.getText());
        DefaultTableModel dtmLivro = (DefaultTableModel) Tabela_livro.getModel();
        Tabela_livro.removeAll();
        dtmLivro.setNumRows(0);
        LivroDAOimplBD instance = new LivroDAOimplBD();
        Livro l = instance.get(codigo);
        dtmLivro.addRow(new Object[]{
                l.getTitulo(),
                l.getAutor(),
                l.getCodigo(),
                l.getIsbn(),
                l.getGenero(),
                l.getMateria_relacionada(),
                l.getEstado_do_livro(),
                l.getStatus(),
                l.getQuant_livros()
            });
    }//GEN-LAST:event_SearchActionPerformed

